    <h2>二、加盟合作细则：</h2>

    <p>2.1  加盟拓客商城须提供联盟商家个人身份证（正反面）、营业执照及相关材料；</p>
    <p>2.2  联盟商家不得提供违法或根据平台规定严禁发布的商品相关信息；</p>
    <p>2.3  联盟商家不得提供假货信息，须确保提供的商品信息真实有效符合中华人民共和国有关法律法规，由此产生的任何交易纠纷自行负责，并承担由于违反前述担保所导致的任何行政责任、民事及刑事法律责任。</p>
    <p>2.4  联盟商家所提供的商品信息各要素应真实合理，且应与商品本身相符，包括商品标题、图片、型号、价格、库存、详情等商品要素。</p>

    <h2>三、合作服务细则：</h2>
    <p>3.1  联盟商家认可拓客商城上的产品及代金券为真实有效并为客户提供相应的产品（或服务）；</p>
    <p>3.2  联盟商家需为所提供产品（或服务）的安全性、服务质量承担全部责任；</p>
    <p>3.3  消费者于拓客商城购买消费券后，凭券号及二维码至指定的拓客联盟商家处消费和体验，联盟商家需提供相应的产品（或服务），不得无理由拒绝；</p>
    <p>3.4  作期间，联盟商家提供的产品（或服务）需真实有效，不得任意更改。如有更改联盟商家需提前7天向我司进行反馈确认并公告，若无提前告知提价，向
    消费者增收费用，一经发现，我司有权进行处罚或终止合作；</p>
    <p>3.5  合作期间，我司将对联盟商家服务及客户满意度进行监控跟踪，客诉率超20%以上的联盟商家，我司有权进行整改及处罚，如整改无效则终止合作。</p>
