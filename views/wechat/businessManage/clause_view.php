    <h2>一、联盟商家加盟拓客共享商城可委托我司进行编辑店铺产品及服务信息，但不得在拓客共享商城上传以下物品资料信息：</h2>

    <p>1.1  毒品、易燃易爆、腐蚀性的化学物品；</p>
    <p>1.2  枪支弹药、火药、及警用和军用物品设备；</p>
    <p>1.3  管制刀具、赌具等其它可能危害他人安全或利益的商品信息；</p>
    <p>1.4  股票、公司债券及任何形式的发票、政府文件及文物；</p>
    <p>1.5 黄色淫秽类禁售的音像制品、含反动、种族歧视或其它法律禁止内容的出版物文件
